#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*rtstr;

	i = 0;
	rtstr = (char *)s;
	while (i < n)
	{
		rtstr[i] = '\0';
		i++;
	}
	s = (void *)rtstr;
}
